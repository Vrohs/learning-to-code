import math

pi = 3.14
print(round(pi))       # 3        # to round off numbers.

print(math.ceil(pi))   # 3        # to round up a number.

print(math.floor(pi))  # 4        # to round down a number kinda similar to the round() function.

print(abs(pi))         # 3.14     # to print the absolute value, like the mod of any number.

print(pow(pi,2))       # 9.8596   # pi raised to the power of 2. 

print(math.sqrt(pi))              # for sqrt of a number.

